package b;

public class Box {
	
	 private int length;
	 private int width;
	 private int heigth;
	 
	 public Box() {
		 
	 }
	 
	public Box(int length, int width, int heigth) {
	
		this.length = length;
		this.width = width;
		this.heigth = heigth;
	}

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeigth() {
		return heigth;
	}

	public void setHeigth(int heigth) {
		this.heigth = heigth;
	}
	
	
	 
	
	
	 
	 
	 

}
