package a;

public class PersonAgeExeption extends Exception{

	public PersonAgeExeption() {
		super();
		
	}

	public PersonAgeExeption(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public PersonAgeExeption(String message, Throwable cause) {
		super(message, cause);
		
	}

	public PersonAgeExeption(String message) {
		super(message);
		
	}

	public PersonAgeExeption(Throwable cause) {
		super(cause);
	}
	
	
	
	

}
